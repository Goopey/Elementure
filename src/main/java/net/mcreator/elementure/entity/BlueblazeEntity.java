	@Override
	protected void registerGoals() {
		super.registerGoals();
		this.goalSelector.addGoal(1, new MeleeAttackGoal(this, 1.2, false) {
			@Override
			protected double getAttackReachSqr(LivingEntity entity) {
				return this.mob.getBbWidth() * this.mob.getBbWidth() + entity.getBbWidth();
			}
		});
		this.goalSelector.addGoal(2, new Goal() {
			{
				this.setFlags(EnumSet.of(Goal.Flag.MOVE));
			}

			public boolean canUse() {
				if (BlueblazeEntity.this.getTarget() != null && !BlueblazeEntity.this.getMoveControl().hasWanted()) {
					return true;
				} else {
					return false;
				}
			}

			@Override
			public boolean canContinueToUse() {
				return BlueblazeEntity.this.getMoveControl().hasWanted() && BlueblazeEntity.this.getTarget() != null && BlueblazeEntity.this.getTarget().isAlive();
			}

			@Override
			public void start() {
				LivingEntity livingentity = BlueblazeEntity.this.getTarget();
				Vec3 vec3d = livingentity.getEyePosition(1);
				BlueblazeEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 1);
			}

			@Override
			public void tick() {
				LivingEntity livingentity = BlueblazeEntity.this.getTarget();
				if (BlueblazeEntity.this.getBoundingBox().intersects(livingentity.getBoundingBox())) {
					BlueblazeEntity.this.doHurtTarget(livingentity);
				} else {
					double d0 = BlueblazeEntity.this.distanceToSqr(livingentity);
					if (d0 < 16) {
						Vec3 vec3d = livingentity.getEyePosition(1);
						BlueblazeEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 1);
					}
				}
			}
		});
		this.targetSelector.addGoal(3, new NearestAttackableTargetGoal(this, AndesiteelementalEntity.class, true, false));
		this.targetSelector.addGoal(4, new NearestAttackableTargetGoal(this, ServerPlayer.class, true, false));
		this.targetSelector.addGoal(5, new HurtByTargetGoal(this));
		this.goalSelector.addGoal(6, new RandomStrollGoal(this, 0.8, 20) {
			@Override
			protected Vec3 getPosition() {
				RandomSource random = BlueblazeEntity.this.getRandom();
				double dir_x = BlueblazeEntity.this.getX() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_y = BlueblazeEntity.this.getY() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_z = BlueblazeEntity.this.getZ() + ((random.nextFloat() * 2 - 1) * 16);
				return new Vec3(dir_x, dir_y, dir_z);
			}
		});
		this.goalSelector.addGoal(7, new RandomLookAroundGoal(this));
	}

